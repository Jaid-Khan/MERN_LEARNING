
// ---------------------- Functions.js ----------------------
// Each exported function below is an event handler for different mouse actions

// Triggered when a button is clicked once
export function click() {
    alert("You Clicked Me !");
}

// Triggered when a button is double-clicked
export function dbl_Click() {
    alert("You Double Clicked Me !");
}

// Triggered when the mouse pointer enters the button area
export function mouseEnter() {
    alert("Mouse Entered");
}

// Triggered when the mouse pointer leaves the button area
export function mouseLeave() {
    alert("Mouse Leaved");
}

